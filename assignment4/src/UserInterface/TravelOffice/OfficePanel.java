    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int selectedRow = customerTable.getSelectedRow();
        if(selectedRow>=0){
            int selectionButton = JOptionPane.YES_NO_OPTION;
            int selectionResult = JOptionPane.showConfirmDialog(null, "Are you sure to delete??","Warning",selectionButton);
            if(selectionResult == JOptionPane.YES_OPTION){
                Customer prod = (Customer)customerTable.getValueAt(selectedRow, 0);
                for(Ticket ticket:prod.getTicket()){
                    out.println(ticket.getCustomer());
                    
                    list.delTicket(ticket);
                    
                    ticket.getSeat().getFlight().returnSeat(ticket);
                }
                office.delCustomer(prod);
                populate();
            }
        }else{
            JOptionPane.showMessageDialog(null, "Please select a Row!!");
        }
    }//GEN-LAST:event_jButton2ActionPerformed



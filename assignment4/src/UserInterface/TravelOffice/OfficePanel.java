    public void populate(){
        DefaultTableModel dtms = (DefaultTableModel)customerTable.getModel();
        dtms.setRowCount(0);
        for(Customer u :office.getCustomer()){
            
            Object[] row = new Object[dtms.getColumnCount()];
            row[0]=u;
            row[1]=u.getUserID();
            row[2]=u.getNickName();
            row[3]=u.getTicketNum();
            dtms.addRow(row);
        }
    }

    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        // TODO add your handling code here:
        String username=usernameTxt.getText();
        String password=String.valueOf(passwordTxt.getPassword());
        if(username==null ||username.equals(""))
        {
            JOptionPane.showMessageDialog(null, "username can not be empty");
            return;
        }
        if(password==null ||password.equals(""))
        {
            JOptionPane.showMessageDialog(null, "password can not be empty");
            return;
        }
        for(TravelOffice a:userList.getOffice()){
            if(a.getUserName().equals(username))
            {
                if(a.verify(password))
                {
                    JOptionPane.showMessageDialog(null, "success");
                    CardLayout layout = (CardLayout)rightPanel.getLayout();
                    rightPanel.add("OfficePanel",new OfficePanel(rightPanel,a,userList));
        
                    layout.next(rightPanel);
                    return;
                }
            }
        }
        JOptionPane.showMessageDialog(null, "username or password error");
    }//GEN-LAST:event_loginActionPerformed
 

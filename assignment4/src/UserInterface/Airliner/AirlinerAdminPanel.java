    public void populate(){
        DefaultTableModel dtm = (DefaultTableModel)tblAirliner.getModel();
        dtm.setRowCount(0);
        for(User u : userList.getAirlineAgency()){
            Airliner a = (Airliner)u;
            Object[] row = new Object[dtm.getColumnCount()];
            row[0]=a.getUserName();
            row[1]=a;
            dtm.addRow(row);
        }
    }


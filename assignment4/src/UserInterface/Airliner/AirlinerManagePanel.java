    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String input = txtSearch.getText();
        if(comboAirAtt.getSelectedItem().equals(" ")){
            DefaultTableModel dtm = (DefaultTableModel) tblAirplane.getModel();
            dtm.setRowCount(0);
            for(Airplane a : airliner.getFleet().getAirplane()){
                
                Object[] row = new Object[dtm.getColumnCount()];
                row[0] = a;
                row[1] = a.getModel();
                row[2] = a.getRow();
                row[3] = a.getColumn();
                row[4] = a.getSerialNumber();
                dtm.addRow(row);
                
            }
        }
        if(comboAirAtt.getSelectedItem().equals("Manufacture")){
            DefaultTableModel dtm = (DefaultTableModel) tblAirplane.getModel();
            dtm.setRowCount(0);
            for(Airplane a : airliner.getFleet().getAirplane()){
                if(a.equals(input)){
                    Object[] row = new Object[dtm.getColumnCount()];
                    row[0] = a;
                    row[1] = a.getModel();
                    row[2] = a.getRow();
                    row[3] = a.getColumn();
                    row[4] = a.getSerialNumber();
                    dtm.addRow(row);
                }
            }
        }
        if(comboAirAtt.getSelectedItem().equals("Seats Range")){
            DefaultTableModel dtm = (DefaultTableModel) tblAirplane.getModel();
            dtm.setRowCount(0);
            for(Airplane a : airliner.getFleet().getAirplane()){
                if((a.getColumn()*a.getRow()) <= Integer.parseInt(txtMax.getText()) && (a.getColumn()*a.getRow()) >= Integer.parseInt(txtMin.getText())){
                    Object[] row = new Object[dtm.getColumnCount()];
                    row[0] = a;
                    row[1] = a.getModel();
                    row[2] = a.getRow();
                    row[3] = a.getColumn();
                    row[4] = a.getSerialNumber();
                    dtm.addRow(row);
                }
            }
        }
        
        if(comboAirAtt.getSelectedItem().equals("Model")){
            DefaultTableModel dtm = (DefaultTableModel) tblAirplane.getModel();
            dtm.setRowCount(0);
            for(Airplane a : airliner.getFleet().getAirplane()){
                if(a.getModel().equals(input)){
                    Object[] row = new Object[dtm.getColumnCount()];
                    row[0] = a;
                    row[1] = a.getModel();
                    row[2] = a.getRow();
                    row[3] = a.getColumn();
                    row[4] = a.getSerialNumber();
                    dtm.addRow(row);
                }
            }
        }
        
        try{
            
            if(comboAirAtt.getSelectedItem().equals("Serial Number")){
                DefaultTableModel dtm = (DefaultTableModel) tblAirplane.getModel();
                dtm.setRowCount(0);
                for(Airplane a : airliner.getFleet().getAirplane()){
                    if(a.getSerialNumber() == Integer.parseInt(input)){
                        Object[] row = new Object[dtm.getColumnCount()];
                        row[0] = a;
                        row[1] = a.getModel();
                        row[2] = a.getRow();
                        row[3] = a.getColumn();
                        row[4] = a.getSerialNumber();
                        dtm.addRow(row);
                    }
                }
            }
            
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Serial Number bust be integer!");
        }
        
        
        try{
            
            if(comboAirAtt.getSelectedItem().equals("Seats")){
                DefaultTableModel dtm = (DefaultTableModel) tblAirplane.getModel();
                dtm.setRowCount(0);
                for(Airplane a : airliner.getFleet().getAirplane()){
                    int s = a.getColumn()*a.getRow();
                    if(s == Integer.parseInt(txtSearch.getText())){
                        Object[] row = new Object[dtm.getColumnCount()];
                        row[0] = a;
                        row[1] = a.getModel();
                        row[2] = a.getRow();
                        row[3] = a.getColumn();
                        row[4] = a.getSerialNumber();
                        dtm.addRow(row);
                    }
                }
            }
            
        }catch(NumberFormatException s){
            JOptionPane.showMessageDialog(null, "Seats must be integer!");
        }
        
        
    }//GEN-LAST:event_btnSearchActionPerformed


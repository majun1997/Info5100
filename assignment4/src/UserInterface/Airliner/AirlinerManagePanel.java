    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int selectedrow = tblAirplane.getSelectedRow();
        if(selectedrow < 0) {
            JOptionPane.showMessageDialog(null, "Please  select a row from table first to view details!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        else{
            Airplane airplane = (Airplane)tblAirplane.getValueAt(selectedrow, 0);
            if(airplane.getFlight()==null)
            {
                airliner.getFleet().deleteAirPlane(airplane);
            }
            else
            {
                if(airplane.isAvailable())
                {
                    airplane.getFlight().getSchedule().setFlight(null);

                    airliner.getFleet().deleteAirPlane(airplane);
                }
                else
                    JOptionPane.showMessageDialog(null, "This airplane is still in mission", "Warning", JOptionPane.WARNING_MESSAGE);
            }
        }
        populate();
    }//GEN-LAST:event_jButton1ActionPerformed


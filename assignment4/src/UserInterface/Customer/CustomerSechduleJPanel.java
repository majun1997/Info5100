    private void sortFlight(String depart,String arrive,String date,String airliner,String airplane){
        initFlight();
        SimpleDateFormat dateFormat=new SimpleDateFormat("YYYY/MM/dd");
        for(Flight f:master.getFlight())
        {
            if((f.getSchedule().getDepart().equals(depart) || depart.equals(""))&&
               (f.getSchedule().getArrive().equals(arrive)||arrive.equals(""))&&
                (dateFormat.format(f.getSchedule().getDate()).equals(date)||date.equals(""))&&
                (airplane.equals(f.getAirplane().getManufacture()+f.getAirplane().getModel())||airplane.equals(""))&&
                (airliner.equals(f.getSchedule().getAgency().getAirline())||airliner.equals(""))
              )
                flight.add(f);
            
            
        }
        populate();
    }

   public void populate(){
        DefaultTableModel dtms = (DefaultTableModel)tableSchedule.getModel();
        dtms.setRowCount(0);
        SimpleDateFormat date=new SimpleDateFormat("YYYY-MM-dd");
        
        for(Flight a:flight){
            
            Object[] row = new Object[dtms.getColumnCount()];
            row[0]=a;
            row[1]=a.getSchedule().getDepart();
            row[2]=a.getSchedule().getArrive();
            
            row[3]=date.format(a.getSchedule().getDate());
            row[4]=a.getSchedule().getDepartTime();
            row[5]=a.getSchedule().getFlightNo();
            row[6]=a.getSchedule().getTourTime();
            row[7]=a.getPrice()[0];
            row[8]=a.getSeatNumber();
            row[9]=a.getAirplane().getManufacture()+a.getAirplane().getModel();
            
            dtms.addRow(row);
            
         }
        }
   
   
   
   
    

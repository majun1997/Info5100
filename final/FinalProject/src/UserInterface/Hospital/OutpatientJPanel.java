    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
        if(selectedRow>=0){
            
            PatientRegister patientRegister = (PatientRegister)jTable1.getValueAt(selectedRow, 0);
            if(patientRegister.getStatus().equals("completed"))
            {
                JOptionPane.showMessageDialog(null, "It has been completed!");
                return; 
            }
            if(patientRegister.getDoctor()==null){
                patientRegister.setDoctor(this.account.getEmployee());
            }
            
            DoctorProcess doctorProcessPanel = new DoctorProcess(this.rightPanel,this.account,this.organization,this.enterprise,this.business,patientRegister);
            this.rightPanel.add("ViewPanel",doctorProcessPanel);
            CardLayout layout=(CardLayout)this.rightPanel.getLayout();
            layout.next(rightPanel);
            
        }else{
            JOptionPane.showMessageDialog(null, "Please select a Patient!!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed


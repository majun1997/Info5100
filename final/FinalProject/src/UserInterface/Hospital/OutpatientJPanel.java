    public void poptable(){
        workqueue.clear();
        for(WorkRequest register: account.getWorkqueue().getWorkRequestList()){
            workqueue.add((PatientRegister)register);
        }
        for(WorkRequest register:organization.getWorkQueue().getWorkRequestList()){
            workqueue.add((PatientRegister)register);
        }
        Collections.sort(workqueue);
        DefaultTableModel model = (DefaultTableModel) this.jTable1.getModel();
        
        model.setRowCount(0);
        
        for (PatientRegister register : workqueue){
            Object[] row = new Object[4];
            row[0] = register;
            row[1] = register.getSender();
            row[2]=register.getDoctor()!=null?register.getDoctor():"waiting for assign";
            row[3]=register.getStatus();
                
            model.addRow(row);
        }
    }
    
    

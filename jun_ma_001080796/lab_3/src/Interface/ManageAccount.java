    public void populateTable()
    {
        DefaultTableModel dtm=( DefaultTableModel)tableDisplay.getModel();
        dtm.setRowCount(0);
        for(Account ac:accountDirectory.getAccountList())
        {
            Object row[]=new Object[4];
            row[0]=ac;
            row[1]=ac.getRoutingNumber();
            row[2]=ac.getAccountNumber();
            row[3]=ac.getBalance();
            dtm.addRow(row);
        }
    }

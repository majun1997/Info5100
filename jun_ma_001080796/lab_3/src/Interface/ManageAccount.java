    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int selectedrow = tableDisplay.getSelectedRow();
        if(selectedrow>=0)
        {
            int a=JOptionPane.YES_NO_CANCEL_OPTION;
            int b=JOptionPane.showConfirmDialog(null, "Would you like to delete the account","WARNING",a);
            if(b==JOptionPane.YES_OPTION)
            {
                Account ac=(Account)tableDisplay.getValueAt(selectedrow, 0);
                accountDirectory.deleteAccount(ac);
                JOptionPane.showMessageDialog(null, "sucessfuly delete");
                populateTable();
            }
           
            
        }
        else
            JOptionPane.showMessageDialog(null, "select a row now");
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int selectedrow = tableDisplay.getSelectedRow();
        if(selectedrow>=0)
        {           
            Account ac=(Account)tableDisplay.getValueAt(selectedrow, 0);
            ViewDetails pane=new ViewDetails(displayJPanel,ac);
            displayJPanel.add("ViewDetails",pane);
            CardLayout layout=(CardLayout)displayJPanel.getLayout();
            layout.next(displayJPanel);
        }
        else
            JOptionPane.showMessageDialog(null, "select a row now");
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        Account ac=accountDirectory.searchAccount(searchT.getText());
        if(ac==null)
        {
            JOptionPane.showMessageDialog(null, "can not find the answer");
        }
        else
        {
            ViewDetails pane=new ViewDetails(displayJPanel,ac);
            displayJPanel.add("ViewDetails",pane);
            CardLayout layout=(CardLayout)displayJPanel.getLayout();
            layout.next(displayJPanel);
        }
    }//GEN-LAST:event_jButton2ActionPerformed


